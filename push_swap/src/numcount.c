#include "../push_swap.h"

int		numcount(char *s)
{
	int count;
	int i;

	i = 0;
	count = 1;
	while (s[i])
	{
		if (s[i] == ' ' || s[i] == '\0')
			count++;
		i++;
	}
	return (count);
}
